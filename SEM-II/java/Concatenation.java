class Concatenation{
	public static void main(String[] args) {
		System.out.println("hello");
		byte a = 10;
		byte b = 20;

		System.out.println(a+b);
		System.out.println("square of "+ a +" is : "+ a * a);
		System.out.println("square of "+ a +" is : "+ (a * a));
		System.out.println(a + b +" is addition of "+ a +" and "+ b);
		System.out.println(a+b +" == "+ a+b);
		System.out.println(a+b +" == "+ (a+b));
	}
}