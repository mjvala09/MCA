# Decimal from
# binary(0b | 0B)	Octal(0o | 0O)	Hexadecimal(0x | 0X)	 

#	first way :
bi=0b11010110
oc=0o214
he=0x214

print("Decimal of 11010110 is : ",int(bi))
print("Decimal of 214 is : ",int(oc))
print("Decimal of 214 is : ",int(he))
#_______________________________________________________
#	second way :
no=14
print("Binary of ",no," is : ",bin(no))
print("Octal of ",no," is : ",oct(no))
print("Hexadecimal of ",no," is : ",hex(no))
